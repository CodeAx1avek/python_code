weeknumber = int(input("Enter week number (1-7): "))
if weeknumber == 1:
    print("Monday")
elif weeknumber == 2:
    print("Tuesday")
elif weeknumber == 3:
    print("Wednesday")
elif weeknumber == 4:
    print("Thursday")
elif weeknumber == 5:
    print("Friday")
elif weeknumber == 6:
    print("Saturday")
elif weeknumber == 7:
    print("Sunday")
else:
    print("Invalid input! Please enter week no. between 1-7.")
    exit(1)
